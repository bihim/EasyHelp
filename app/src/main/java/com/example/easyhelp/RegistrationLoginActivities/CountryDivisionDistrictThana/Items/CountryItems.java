package com.example.easyhelp.RegistrationLoginActivities.CountryDivisionDistrictThana.Items;

public class CountryItems
{
    private String name;
    private String id;

    public CountryItems(String name, String id)
    {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }
}
